public class Zapis {
    private String nazwa;
    private int nrLamiglowki;

    public Zapis(String nazwa, int nrLamiglowki) {
        this.nazwa = nazwa;
        this.nrLamiglowki = nrLamiglowki;
    }

    public String getNazwa() {
        return nazwa;
    }

    public int getNrLamiglowki() {
        return nrLamiglowki;
    }
}
